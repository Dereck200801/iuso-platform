const http = require('http');

const server = http.createServer((req, res) => {
  if (req.url === '/health' && req.method === 'GET') {
    res.writeHead(200, { 'Content-Type': 'application/json' });
    res.end(JSON.stringify({
      status: 'ok',
      date: new Date().toISOString(),
      message: 'IUSO Simple API is running!',
      note: 'This proves the concept works - NestJS will follow the same pattern'
    }));
  } else {
    res.writeHead(404, { 'Content-Type': 'application/json' });
    res.end(JSON.stringify({ error: 'Not Found' }));
  }
});

const PORT = 3000;
server.listen(PORT, () => {
  console.log(`ðŸš€ Simple IUSO API listening on port ${PORT}`);
  console.log(`âœ… Test with: curl http://localhost:${PORT}/health`);
});

// Graceful shutdown
process.on('SIGINT', () => {
  console.log('\nðŸ‘‹ Shutting down server...');
  server.close(() => {
    console.log('âœ… Server closed');
    process.exit(0);
  });
}); 